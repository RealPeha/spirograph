const PI = Math.PI
const E = Math.E
const exp = n => Math.exp(n)
const abs = n => Math.abs(n)
const sqrt = n => Math.sqrt(n)
const pow = (n, n1) => Math.pow(n, n1)
const cos = n => Math.cos(n)
const sin = n => Math.sin(n)
const tg = n => Math.tan(n)
const ctg = n => cos(n) / sin(n)
const sec = n => 1 / cos(n)
const cosec = n => 1 / sin(n)
const log = n => Math.log(n)
const round = n => Math.round(n)
const RND = (min, max) => Math.random() * (max + 1 - min) + min